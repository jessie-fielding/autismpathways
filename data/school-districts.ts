export function getDistrictByState(query: string): SchoolDistrictState | undefined {
  const q = query.trim().toLowerCase();
  return SCHOOL_DISTRICTS.find(
    d => d.state.toLowerCase() === q || d.abbr.toLowerCase() === q
  );
}

export function searchDistricts(query: string): SchoolDistrictState[] {
  const q = query.trim().toLowerCase();
  if (!q) return SCHOOL_DISTRICTS;
  return SCHOOL_DISTRICTS.filter(
    d =>
      d.state.toLowerCase().includes(q) ||
      d.abbr.toLowerCase().includes(q) ||
      d.seaName.toLowerCase().includes(q)
  );
}
